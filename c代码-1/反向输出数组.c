//反向输出有5个元素的int数组中的各元素的值。数组为{10,20,30,40,50}。
//**输出格式要求："\ta[%d]=%d,"
//程序运行示例如下：
//	a[4]=50,	a[3]=40,	a[2]=30,	a[1]=20,	a[0]=10,
#include <stdio.h>
int main()
{
	int a[5]={10,20,30,40,50,};
	int i;
	int t;
	for(i=4;i>=0;i--)
	{
		t=a[i];
		printf("\ta[%d]=%d, ",i,t);
		printf("%c",8) ;
	}
	return 0;
}
