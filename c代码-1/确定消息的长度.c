


//一个首项大于0的等差数列的前四项的和为26，前四项的积为880，求此数列。
//**输出格式要求："%d,"  "......\n"   (只输出前21个数，其他为......)
//程序运行示例如下：
//2,5,8,11,14,17,20,23,26,29,32,35,38,41,44,47,50,53,56,59,62,......
#include <stdio.h>
int main()
{   
 int x=2;   
 int step=3;    
  int i; 
  for(i=0;i<21;i++)
  {
  	printf("%d,",x+i*step);
  }
  printf("......");
							  
}


